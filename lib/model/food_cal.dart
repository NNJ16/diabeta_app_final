class FoodCal{
  final String name;
  final double serveSize;
  final double calorie;
  final double totFat;
  final double satFat;
  final int sodium;
  final double carbs;
  final double fiber;
  final double protein;
  final double sugar;

  const FoodCal({
    required this.name,
    required this.serveSize,
    required this.calorie,
    required this.totFat,
    required this.satFat,
    required this.sodium,
    required this.carbs,
    required this.fiber,
    required this.protein,
    required this.sugar
  });
}